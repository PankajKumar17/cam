class TestDatasetGeneration:
    """Verify the synthetic dataset has correct shape, schema, and labels."""

    def test_csv_exists(self):
        """Output CSV must exist on disk."""
        assert os.path.exists(RAW_CSV), f"Missing: {RAW_CSV}"

    def test_row_count(self, raw_df):
        """Should have 200+ rows (15 defaulted x ~8yr + 15 healthy x 16yr)."""
        assert len(raw_df) >= 200, f"Only {len(raw_df)} rows — expected 200+"

    def test_expected_columns(self, raw_df):
        """At least 50 key columns must be present."""
        missing = [c for c in EXPECTED_COLUMNS if c not in raw_df.columns]
        assert len(missing) == 0, f"Missing columns: {missing}"
        assert len(raw_df.columns) >= 50, (
            f"Only {len(raw_df.columns)} columns — expected 50+"
        )

    def test_label_has_both_classes(self, raw_df):
        """Label column must contain both 0 (healthy) and 1 (defaulted)."""
        unique = set(raw_df["label"].unique())
        assert 0 in unique, "No healthy companies (label=0) in dataset"
        assert 1 in unique, "No defaulted companies (label=1) in dataset"

    def test_no_fully_null_columns(self, raw_df):
        """No column should be entirely NaN."""
        fully_null = [c for c in raw_df.columns if raw_df[c].isna().all()]
        assert len(fully_null) == 0, f"Fully null columns: {fully_null}"

    def test_company_count(self, raw_df):
        """Should have 30 companies (15 defaulted + 15 healthy)."""
        assert raw_df["company_name"].nunique() == 30


