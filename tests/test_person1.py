#  TEST 6: No data leakage
# ============================================================================

class TestNoDataLeakage:
    """
    Verify there is no temporal leakage: training uses only past data,
    testing uses only future data.
    """

    def test_temporal_split_is_clean(self, feature_df):
        """
        The credit scorer splits at FY<=2017 for train, FY>2017 for test.
        Verify the split boundary exists in the data.
        """
        model_path = os.path.join(MODELS_DIR, "xgb_model.pkl")
        if not os.path.exists(model_path):
            pytest.skip("Models not trained")

        from modules.person1_ml_core.credit_scorer import prepare_data

        data = prepare_data(feature_df)

        # The test df should only have years > split_year
        test_years = data["df_test"]["fiscal_year"]
        train_years = feature_df.loc[
            feature_df["fiscal_year"] <= 2017, "fiscal_year"
        ]

        assert test_years.min() > 2017, (
            f"Test set contains year {test_years.min()} <= 2017 (leakage!)"
        )
        assert train_years.max() <= 2017, (
            f"Train set contains year {train_years.max()} > 2017 (leakage!)"
        )

    def test_train_test_no_company_overlap_in_lstm(self, feature_df):
        """
        LSTM splits by company — train and test companies must not overlap.
        """
        model_path = os.path.join(MODELS_DIR, "lstm_trajectory_model.pt")
        if not os.path.exists(model_path):
            pytest.skip("LSTM model not trained")

        from modules.person1_ml_core.temporal_model import (
            build_sequences, split_by_company,
        )

        sequences, labels, companies = build_sequences(feature_df)
        data = split_by_company(sequences, labels, companies)

        overlap = data["train_companies"] & data["test_companies"]
        assert len(overlap) == 0, (
            f"Company overlap between train/test: {overlap}"
        )


