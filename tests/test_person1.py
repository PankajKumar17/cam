        row["revenue_growth"] = 0.50      # but high revenue growth
        row["promoter_pledge_pct"] = 0.90  # extreme pledge
        row["dscr"] = 0.3                 # very low DSCR
        row["interest_coverage"] = 0.5    # can't cover interest
        row["beneish_m_score"] = 1.5      # looks manipulated

        result = predict(row)
        # With contradictory signals, disagreement > 0 is expected
        assert result["model_disagreement"] >= 0, (
            "Contradictory row should produce some disagreement"
        )


